#include "lcd.h"

void delay(UINT16 i)
{
    UINT16 j,k;
   for(j=0;j<i;j++)
     for(k=0;k<110;k++);
}

void send_command(UINT8 data)
{
    UINT8 i;
   PIN_LCD_SEL=0;//Selected LCD
   PIN_LCD_A0_RS=0;//command
   for(i=0;i<8;i++)
   {
       PIN_LCD_SCLK=0;
     if(data&0x80)
         PIN_LCD_SDATA=1;
     else
         PIN_LCD_SDATA=0;
     PIN_LCD_SCLK=1;
     data= data <<1;
   }
}

void send_data(UINT8 data)
{
    UINT8 i;
   PIN_LCD_SEL=0;//Selected LCD
   PIN_LCD_A0_RS=1;;//data
   for(i=0;i<8;i++)
   {
       PIN_LCD_SCLK=0;
     if(data&0x80)
         PIN_LCD_SDATA=1;
     else
         PIN_LCD_SDATA=0;
     PIN_LCD_SCLK=1;
     data= data <<1;
   }
}

